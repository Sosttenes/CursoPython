# Par ou ímpar
# Desenvolva um programa que leia um número inteiro e mostre se ele é PAR ou ÍMPAR.
num = int(input('Digite um número: ')) # Pede um número ao usuário
if num  % 2 == 0: # Se o número dividido por 2 tiver resto 0
    print(f'O número {num} é par!') # Mostra que é par
else: # Senão
    print(f'O número {num} é ímpar') # Mostra que é ímpar
