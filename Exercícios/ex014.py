# Conversor de temperaturas
# Faça um programa que leia uma temperatura em graus Celsius e a converta para graus Fahrenheit e Kelvin.
# Fórmulas:
# F = C*1.8+32
# K = C+273.15
# Temperatura em Celsius
c = float(input('Digite a temperatura em graus Celsius: ')) # Pega a temperatura em Celsius
f = c*1.8+32 # Converte a temperatura para Fahrenheit
k = c+273.15 # Converte a temperatura para Kelvin
print(f'A temperatura de {c}°C é equivalente a {f}°F e {k}K') # Mostra a temperatura em Fahrenheit e Kelvin
