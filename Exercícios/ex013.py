# Reajuste salarial
# Faça um algoritmo que leia o salário de um funcionário e mostre seu novo salário, com 15% de aumento.
# Salário do funcionário
s = float(input('Digite o salário do funcionário: R$', )) # Pega o salário do funcionário
a = s*(15/100) # Calcula o aumento de 15%
ns = s+a # Calcula o novo salário do funcionário
print(f'O salário do funcionário é de R${s:.2f} e com 15% de aumento, o novo salário é de R${ns:.2f}')
