# Dobro, Triplo e Raiz Quadrada de um número
from math import sqrt # Importa a função raiz quadrada
n = int(input('Digite um número: '))
print(f'O dobro de {n} é: {n*2}')
print(f'O triplo de {n} é: {n*3}')
print(f'A raiz quadrada de {n} é: {sqrt(n)}') # Função raiz quadrada
