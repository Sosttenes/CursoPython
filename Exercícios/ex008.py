# Conversor de medidas
m = float(input('Digite uma distância em metros: ')) # Pega a distância em metros
print(f'A medida de {m}m corresponde a:') # Mostra a distância em metros
print(f'{m/1000}km') # Mostra a distância em quilômetros
print(f'{m/100}hm') # Mostra a distância em hectômetros
print(f'{m/10}dam') # Mostra a distância em decâmetros
print(f'{m*10:.0f}dm') # Mostra a distância em decímetros
print(f'{m*100:.0f}cm') # Mostra a distância em centímetros
print(f'{m*1000:.0f}mm') # Mostra a distância em milímetros
