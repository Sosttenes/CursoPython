# Média aritmética de duas notas
n1 = float(input('Digite a primeira nota: ')) # Pega a primeira nota
n2 = float(input('Digite a segunda nota: ')) # Pega a segunda nota 
# A função float é pra transformar o valor em número real
m = (n1+n2)/2
print(f'A média entre {n1} e {n2} é igual a {m:.1f}') # :.1f é pra mostrar apenas uma casa decimal
